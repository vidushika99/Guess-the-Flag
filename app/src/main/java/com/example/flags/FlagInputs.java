package com.example.flags;

public class FlagInputs {

    String nameC;
    int imagesF;

    public FlagInputs(String nameC, int imagesF) {
        this.nameC = nameC;
        this.imagesF = imagesF;
    }

    public String getNameC() {
        return nameC;
    }

    public int getImagesF() {
        return imagesF;
    }
}
